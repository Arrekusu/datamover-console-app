CREATE TABLE IF NOT EXISTS CUSTOMERS
(
    ALIAS VARCHAR(2147483647),
    FIRST_NAME VARCHAR(2147483647),
    LAST_NAME VARCHAR(2147483647),
    ACCOUNT_CODE VARCHAR(2147483647)
);
CREATE TABLE IF NOT EXISTS ACCOUNTS
(
    CODE VARCHAR(2147483647),
    TYPE VARCHAR(2147483647),
    NUMBER VARCHAR(2147483647)
);
CREATE TABLE IF NOT EXISTS ORDERS
(
    USER VARCHAR(2147483647),
    SUMMARY VARCHAR(2147483647),
    NOTES VARCHAR(2147483647)
);

